//  This defines what actually goes into an attribute
//  The pointer should be cast to point to it.

#ifndef SVLIS_U_ATTRIB
#define SVLIS_U_ATTRIB


class sv_user_attribute
{
public:
	void* pointer;
	sv_integer ref_count;
	sv_lock lock;           // Data locking for parallel machines

	sv_user_attribute() { pointer = 0; } // Null constructor

	sv_user_attribute(void* p) { pointer = p; ref_count = 1; }
};

#endif

